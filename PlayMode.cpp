bool PlayMode::handle_event(SDL_Event const &evt, glm::uvec2 const &window_size) {
	if (evt.type == SDL_KEYDOWN) {
		if (evt.key.keysym.sym == P1_LEFT) {
			player1.left.pressed = true;
			return true;
		}
	}

	return false;
}

